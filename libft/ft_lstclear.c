#include "libft.h"

void	ft_lstclear(t_node **lst, void (*del)(void *))
{
	t_node	*node;
	t_node	*next;

	if (!lst || !del || !*lst)
		return ;
	node = *lst;
	while (node)
	{
		next = node->next;
		ft_lstdelone(node, del);
		node = next;
	}
	*lst = NULL;
	free (lst);
}
